numero_1 =float (input ("Primer número = "))
Operación =input("Operacion (+,-,/,*)= ")
numero_2 =float (input("Segundo número = "))
if Operación  == "+":
 print  (f"{numero_1} + {numero_2} = {numero_1 + numero_2}")
